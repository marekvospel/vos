    0b0000000_0000000_0011100_0100010_0000010_0100010_0011100_0000000_0000000;
const LETTER_SPACE: u64 = 0;
const REPLACEMENT_CHARACTER: u64 =
    0b0000000_0001000_0010100_0111110_1110111_1101111_0100010_0011100_0001000;

pub fn resolve_letter(letter: char) -> u64 {
    match letter {
        'A' => LETTER_CAPITAL_A,
        'B' => LETTER_CAPITAL_B,
        'C' => LETTER_CAPITAL_C,
        'D' => LETTER_CAPITAL_D,
        'E' => LETTER_CAPITAL_E,
        'F' => LETTER_CAPITAL_F,
        'G' => LETTER_CAPITAL_G,
        'H' => LETTER_CAPITAL_H,
        'I' => LETTER_CAPITAL_I,
        'J' => LETTER_CAPITAL_J,
        'K' => LETTER_CAPITAL_K,
        'L' => LETTER_CAPITAL_L,
        'M' => LETTER_CAPITAL_M,
        'N' => LETTER_CAPITAL_N,
        'O' => LETTER_CAPITAL_O,
        'P' => LETTER_CAPITAL_P,
        'Q' => LETTER_CAPITAL_Q,
        'R' => LETTER_CAPITAL_R,
        'S' => LETTER_CAPITAL_S,
        'T' => LETTER_CAPITAL_T,
        'U' => LETTER_CAPITAL_U,
        'V' => LETTER_CAPITAL_V,
        'W' => LETTER_CAPITAL_W,
        'X' => LETTER_CAPITAL_X,
        'Y' => LETTER_CAPITAL_Y,
        'Z' => LETTER_CAPITAL_Z,
        'a' => LETTER_SMALL_A,
        'b' => LETTER_SMALL_B,
        'c' => LETTER_SMALL_C,
        ' ' => LETTER_SPACE,
        _ => REPLACEMENT_CHARACTER,
    }
}
