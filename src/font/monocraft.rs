const LETTER_CAPITAL_B: u64 =
    0b0000000_0000000_0011110_0100010_0100010_0100010_0011110_0100010_0011110;
const LETTER_CAPITAL_C: u64 =
    0b0000000_0000000_0011100_0100010_0000010_0000010_0000010_0100010_0011100;
